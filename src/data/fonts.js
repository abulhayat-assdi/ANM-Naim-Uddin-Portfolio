// Helper to get array of font families for whitelisting
export const fontNames = fontList.map(font => font.family);

// Helper to load a font dynamically
export const loadFont = (fontFamily) => {
    const font = fontList.find(f => f.family === fontFamily);
    if (font && font.url) {
        if (!document.querySelector(`link[href="${font.url}"]`)) {
            const link = document.createElement('link');
            link.href = font.url;
            link.rel = 'stylesheet';
            document.head.appendChild(link);
        }
    }
};
